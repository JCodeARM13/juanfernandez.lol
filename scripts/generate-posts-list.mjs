import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import matter from "gray-matter";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const POSTS_DIR = path.join(ROOT, "blog");
const OUT_FILE = path.join(ROOT, "lib", "generated-posts.ts");

function read() {
  if (!fs.existsSync(POSTS_DIR)) return [];
  return fs
    .readdirSync(POSTS_DIR)
    .filter((f) => f.endsWith(".md"))
    .map((f) => {
      const slug = f.replace(/\.md$/, "");
      const raw = fs.readFileSync(path.join(POSTS_DIR, f), "utf8");
      try {
        const { data } = matter(raw);
        if (!data.title || !data.date) return null;
        const date =
          typeof data.date === "string"
            ? data.date
            : new Date(data.date).toISOString().slice(0, 10);
        return {
          slug,
          title: data.title,
          description: data.description ?? null,
          date,
          author: data.author ?? null,
          tags: Array.isArray(data.tags) ? data.tags.map(String) : [],
          cover: data.cover ?? null,
        };
      } catch {
        return null;
      }
    })
    .filter(Boolean)
    .sort((a, b) => (a.date < b.date ? 1 : -1));
}

const posts = read();
const banner = "// Auto-generated by scripts/generate-posts-list.mjs. Do not edit.\n";
const body = `export type GeneratedPost = {
  slug: string;
  title: string;
  description: string | null;
  date: string;
  author: string | null;
  tags: string[];
  cover: string | null;
};

export const GENERATED_POSTS: GeneratedPost[] = ${JSON.stringify(posts, null, 2)};
`;
fs.writeFileSync(OUT_FILE, banner + body);
console.log(`✓ Wrote ${posts.length} posts to ${path.relative(ROOT, OUT_FILE)}`);
